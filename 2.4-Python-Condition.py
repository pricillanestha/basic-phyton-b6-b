# Python : Condition (If, elif, else) 
# (Condition : ==, !=, >,<,>=, and, or, not)

# x = 2
# y = 22
x = int(input("Masukkan x :"))
y = int(input("masukkan y :"))

if x > y :
    print ("x itu lebih besar dari y")
elif x < y :
    print ("x itu kurang dari y")
elif x == y:
    print ("x dan y itu sama")
else :
    print ("salah input !")